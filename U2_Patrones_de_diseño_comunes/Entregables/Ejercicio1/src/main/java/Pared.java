
public class Pared {
	
	private Double altura;

	public Pared(Double altura) {
		super();
		this.altura = altura;
	}

	public Double getAltura() {
		return altura;
	}

	public void setAltura(Double altura) {
		this.altura = altura;
	}

	@Override
	public String toString() {
		return "Pared [altura=" + altura + "]";
	}
	
	

}
